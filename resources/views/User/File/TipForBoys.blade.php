        <h3 style="color: #1a202c; margin-left: 6px;" id="tip_1"><b> 1.Don’t discredit her feelings.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            A lot of men have a tendency to invalidate their partner’s feelings. “For example, they might say, ‘That’s a
            silly thing to be mad about,’ or ‘I can’t believe that upset you,” explains premarital counselor, Rachel
            Lamson. “Instead, try to validate your partners’ emotions by apologizing for being the source of that
            emotion without admitting guilt,” <br> <br>

            For example, saying something like, “I’m sorry that my actions caused you to feel upset. It wasn’t my
            intention,” sounds a lot better.
        </p>
        <h3 style="color: #1a202c;margin-left: 6px; " id="tip_2"><b>2.Standup for her and others.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            When a man sees a situation that needs to be dealt with, he should step forward and deal with it. If someone
            is being rude to your partner, women still appreciate a man stepping up to call them on it. Make sure you’re
            there to defend and protect her. It doesn’t mean she can’t defend herself, it just means chivalry isn’t
            dead.
        </p>
        <h3 style="color: #1a202c;margin-left:6px;" id="tip_3"><b>3.Talk about what’s bothering you.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            Another common tendency a lot of men have in relationships, is shutting down when they feel hurt instead of
            expressing it. Nobody benefits from silence or keeping things that are bugging you bottled up. <br> <br>

            “Instead, take 10-30 minutes to calm down and work through your emotions, in order to them into words, ”
            says Lamson. “Then approach your partner to explain how you feel.”
        </p>
        <h3 style="color: #1a202c; margin-left: 6px;" id="tip_4"><b>4.Share with your partner.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            Men are notorious for withholding their needs/emotions/fears/hopes. But times have changed, and a man who
            can talk about his feelings is what the majority of women want and need. <br> <br>

            “Men may worry they’ll appear weak,” says Lamson. “But more likely their partner will see strength in their
            ability to be open and honest. It’ll make both people feel closer and more supported.”
        </p>

        <h3 style="color: #1a202c; margin-left: 6px;" id="tip_5"><b>5.Don’t take her not wanting sex, personally.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            You may feel like you’re not getting the attention you want, but if your partner is preoccupied with work,
            kids, errands, or just life in general it’s not that she doesn’t care about you or love you, but it’s very
            difficult to put all that aside. While it’s crucial to have alone time and intimacy with your partner,
            understand how challenging it might be for her to refocus her energy on you and don’t take it as a sign of
            rejection.
        </p>
        <h3 style="color: #1a202c;margin-left: 6px;" id="tip_6"><b>6.Listen to your partner.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            For men to have a successful relationship with a woman they need to actually go against their inherent
            nature to fix problems and become better active listeners. <br> <br>

            “Men hear all the time from women that they want men to listen and not fix the problem,” explains Dr. Dori
            Gatter, a licensed Psychotherapist and Relationship Expert. <br> <br>

            Sometimes being supportive, means being there for someone and not telling them what they should do or how
            they should feel. Some problems can’t be fixed, but by listening you can help.
        </p>
        <h3 style="color: #1a202c;margin-left: 6px;" id="tip_7"><b>7.Be more decisive.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            We’re not just talking about picking the restaurant or what movie to watch, but also about bigger, life
            decisions. To many women, a man who avoids making decisions is skirting his responsibilities. Being wishy
            washy on issues related to career, family, or even the relationship is a total turnoff.
        </p>
        <h3 style="color: #1a202c;margin-left: 6px;" id="tip_8"><b>8.Pick up some of the slack.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            Between taking care of the children, making dinner, and keeping the home together, not to mention working,
            women have a lot on their plate. Women want to be your partner, she doesn’t want another person to take care
            of. Picking up some of the slack provides her tremendous relief and she sincerely appreciates it. Don’t wait
            for her to ask. Just do it.
        </p>
        <h3 style="color: #1a202c;margin-left: 6px;" id="tip_9"><b>9.It doesn’t matter how, just make her feel special.</b></h3>
        <p style="margin: 10px; color: #2d3748;">
            Men may not be inclined to express themselves emotionally, but regardless, you have to let your partner know
            how much you like them by being around them and taking care of them. You can tell her through words or even
            physically. Guys may be able to go without many of these things, but to many women this is her oxygen. <br>
            <br>

            Every women is different and how they deal with things like stress, family, affection, arguments, and
